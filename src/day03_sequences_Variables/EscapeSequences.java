package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello Every One \n\tMY Name is Cybertek");
        System.out.println("\tI am in Virginia\n\n\n");
        System.out.println("\t\t\tI Love programming");

        System.out.println("\\");
        System.out.println("My name is \'Igor\'");
        System.out.println("My name is \"Igor\"");

    }
//Escape Sequences:

// When adding \n makes a Newline
//             \t makes a Horizontal tab(New Space button)
//             \\ makes Backslash
//             \' makes a Single quote
//             \" makes a Double quote

}
