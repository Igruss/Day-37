package day04_Variables;
/*
    char it can only be used for a single character
    MUST be given in a single '   ('A')
 */
public class Variables2 {

    public static void main(String[] args) {

         char ch1= 'A';

        System.out.println(ch1);

         char ch2= 65;
        System.out.println(ch2);

        char ch3=6984;
        System.out.println(ch3);

        char c1='A';  //65
        char c2='B';  //66

        System.out.println(c1+c2);
        // When applying + (addition) it's always gonna be as a NUBMER

        char c4=65+45;
        System.out.println(c4);//131

        char c5='1';//49
        System.out.println(c5);
        System.out.println(c5+17);//49+17=66
























    }


















}
