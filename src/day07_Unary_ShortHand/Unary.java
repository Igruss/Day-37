package day07_Unary_ShortHand;

public class Unary {

    public static void main(String[] args) {

        //--:decrement(decrease value by 1)
        int a =20;
        --a;
        System.out.println(a);

        //==: increment(increase value by 1)
        int b=10;
        ++b;
        System.out.println(b);




    }



}
